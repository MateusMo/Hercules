void hercules_animation(const char *str) {

    char spin[] = "|/-\\";
    int frames = 12;

    for (int i = 0; i < frames; i++) {
        clear_screen();

        printf(GOLD "%s" RESET, templo_base);
        printf("\n");

        printf(CYAN "[%c] Hercules is cleaning with strength:%s...\n" RESET,
               spin[i % 4], str);

        printf("[");
        int progress = (i + 1) * (100 / frames);

        for (int j = 0; j < 50; j++) {
            if (j < progress)
                printf(GREEN "#");
            else
                printf(" ");
        }

        printf(RESET "] %d%%\n", progress * 2);

        fflush(stdout);
        usleep(200000);
    }

    clear_screen();
}

void clear_screen() {
    printf("\033[H\033[J");
}

